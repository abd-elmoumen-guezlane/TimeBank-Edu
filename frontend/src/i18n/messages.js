export function pickMessage(locale, key) {
  const parts = key.split('.');
  const read = (tree) => {
    let n = tree;
    for (const p of parts) {
      n = n?.[p];
    }
    return typeof n === 'string' ? n : null;
  };
  return read(messages[locale] || {}) || read(messages.fr) || key;
}
